package org.example.hw3;

public class Task1 {
    public static void main(String[] args) {
        String[][] products = {
                {"Высший сорт апельсинов", "150", "1"},
                {"Средний сорт яблок", "80", "2"},
                {"Высший сорт бананов", "120", "1"},
                {"Средний сорт винограда", "200", "2"},
                {"Высший сорт мандаринов", "180", "1"},
                {"Средний сорт персиков", "100", "2"}
        };

        int maxPrice = 0;

        for (String[] product : products) {
            String name = product[0];
            int price = Integer.parseInt(product[1]);
            int sort = Integer.parseInt(product[2]);

            if (name.contains("Высший") && (sort == 1 || sort == 2)) {
                if (price > maxPrice) {
                    maxPrice = price;
                }
            }
        }

        System.out.println("Наибольшая цена товаров высшего сорта: " + maxPrice);
    }
}

//    В этом примере мы используем двумерный массив products, который содержит записи о товарах: наименование товара, цена и сорт. Затем мы перебираем все записи в массиве с помощью цикла for-each.
//        Для каждой записи мы проверяем, содержит ли название товара строку "высший", а также является ли сорт товара 1-м или 2-м.
//        Если это так, то мы сравниваем цену товара с текущей наибольшей ценой и, если она больше, обновляем значение переменной maxPrice.
//        Наконец, мы выводим наибольшую цену товаров высшего сорта в стандартный вывод.